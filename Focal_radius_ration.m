function k=Focal_radius_ration(n)
%==========================================================================
%本函数是用来计算凸透镜的焦距与凸透镜的半径的比率
%输入的是凸透镜材质的折射率n
%输入的是凸透镜焦距f与凸透镜半径的比率
%创建人：毛家发
%创建时间：2021-10-26
%完成时间：2021-10-26
%==========================================================================
a=n*(1+(pi/2)*(pi/2))*(1+(pi/2)*(pi/2));
b=pi*pi*pi*(n-1)*(n-1);
k=a/b;