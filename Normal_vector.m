function sat=Normal_vector(afai,f,r,n) 
%==========================================================================
%本程序是用来求解凸透镜的法向量函数
%输入的是入射点afai、凸透镜的焦距f、凸透镜的半径、折射率n
%输出的是入射点afai处的法向量
%创建人：毛家发
%创建时间：2021-10-17
%完成时间：2021-10-10
%==========================================================================
p=atan(f/Mapping_d2r(afai,r));
sat=atan(abs(n*cos(p)/(n*sin(p)-1)));
