function x=Mapping_d2r(afai,r)
%==========================================================================
%本程序的功能是将入射点afai映射成长度(mm)
%输入的是入射点afai，输出的是该点对应的长度
%创建人：毛家发
%创建时间：2021-10-17
%完成时间：2021-10-17
%=====================================
if(afai==pi/2)
    x=r;
else
    x=r*(pi/2-afai)*tan(afai);
end
